//Variável que guarda o número do usuário.
var n = parseFloat(prompt("Digite o número: "));

//Fazendo a checagem de se é par ou impar.
if (n % 2 === 0) {

    //Alert avisando que é par.
    alert("O número que você digitou é par!");
} else {

    //Alert avisando que é impar.
    alert("O número que você digitou é impar!");
}
