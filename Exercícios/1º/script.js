//Variáveis que guardam o valor do usuário.
var n1 = parseFloat(prompt("Digite o primeiro número: "));
var n2 = parseFloat(prompt("Digite o segundo número: "));

//Soma.
var soma = n1 + n2;

//Subtração.
var sub = n1 - n2;

//Multiplicação.
var multi = n1 * n2;

//Divisão.
var divi = n1 / n2;

//Alert mostrando cada operação matemática.
alert("A soma de " + n1 + " mais " + n2 + " é de: " + soma);
alert("A subtração de " + n1 + " menos " + n2 + " é de: " + sub);
alert("A multiplicação de " + n1 + " mais " + n2 + " é de: " + multi);
alert("A divisão de " + n1 + " mais " + n2 + " é de: " + divi);